nombre = "AlexCode"

print(len(nombre))#ver longitud de cadena

print(nombre.find("e"))#Buscar posicion de caracter

print(nombre.capitalize())#Poner la primera letra en mayuscula

print(nombre.upper())#Texto a mayusculas

print(nombre.lower())#Texto a minusculas

print(nombre.isdigit())#Verificar si es un digito o no

print(nombre.isalpha())#Verificar si son caracteres alphabeticos

print(nombre.count("e"))#Contar cuantos caracteres hay

print(nombre.replace("x","o"))#Remplazar Caracter

print(nombre*4)#Multiplica el caracter