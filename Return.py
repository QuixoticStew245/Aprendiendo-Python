# ---------------------
# Definimos una función llamada 'multiplicar'
# ---------------------
# Esta función recibe dos parámetros: 'a' y 'b'
# La función devuelve el resultado de multiplicar a * b
def multiplicar(a, b):
    return a * b  # 'return' envía el resultado fuera de la función

# ---------------------
# Llamamos a la función y guardamos el resultado en la variable 'x'
# ---------------------
x = multiplicar(3, 4)  # multiplica 3 * 4 y guarda 12 en 'x'

# ---------------------
# Mostramos el resultado
# ---------------------
print(x)  # Imprime: 12
